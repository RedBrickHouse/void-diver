(function () {
  var LANGS = ["ko","zh","en","ja"];
  var HTMLLANG = { en:"en", ko:"ko", ja:"ja", zh:"zh-CN" };
  function apply(lang) {
    if (LANGS.indexOf(lang) < 0) lang = "ko";
    var dict = window.I18N[lang];
    document.documentElement.setAttribute("lang", HTMLLANG[lang]);
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n")];
      if (v != null) el.innerHTML = v;
    });
    var lf = (lang === "zh") ? "logo-cn.png" : "logo.png";
    document.querySelectorAll("img.game-logo").forEach(function (im) {
      im.src = (im.getAttribute("data-logo") || "") + lf;
    });
    document.querySelectorAll(".lang-btn").forEach(function (b) {
      b.classList.toggle("active", b.getAttribute("data-lang") === lang);
    });
    try { localStorage.setItem("vd_lang", lang); } catch (e) {}
  }
  window.setLang = apply;
  function initial() {
    try {
      var q = new URLSearchParams(location.search).get("lang");
      if (q && LANGS.indexOf(q) >= 0) return q;
    } catch (e) {}
    var l = null;
    try { l = localStorage.getItem("vd_lang"); } catch (e) {}
    if (l && LANGS.indexOf(l) >= 0) return l;
    var n = (navigator.language || "ko").slice(0,2);
    return LANGS.indexOf(n) >= 0 ? n : "ko";
  }
  if (document.readyState !== "loading") apply(initial());
  else document.addEventListener("DOMContentLoaded", function () { apply(initial()); });
})();

